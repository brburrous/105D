# ╔═╡ ab206e00-1d59-4297-9157-6982a82ddcd2
function Ra(Ts, Tinf, L, prop) 
	g = 9.8u"m/s^2"
	β = 1/Tinf
	Gr = g*β*(Ts-Tinf)*L^3/prop.ν^2
	Ra = Gr*prop.Pr
end

# ╔═╡ a644bcfe-2603-4c32-887e-9fe660277ed5
function Nu_Cyl(Ts, Tinf, D, prop)
	Ra_D = Ra(Ts, Tinf, D, prop)
	Pr = prop.Pr
	Nu_D = (0.60 + 0.387*Ra_D^(1/6)/((1+(0559*Pr)^(916))^(8/27)))^2
end

